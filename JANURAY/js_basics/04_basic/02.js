const month = "03"

switch (month){
    case "01":
        console.log("jan");
        break;
    case "02":
        console.log("feb");
        break;
    case "03":
        console.log("march");
        break;
    case "04":
        console.log("april");
        break;
    default:
        console.log("default case match");
        break;
}