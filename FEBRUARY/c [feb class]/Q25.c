// C program to illustrate the relational operators

#include<stdio.h>

int main(){
    int a = 25;
    int b = 5;

    printf("a < b :%d\n", a < b);
    printf("a > b : %d\n", a > b);
    printf("a <= b: %d\n", a <= b);
    printf("a >= b: %d\n", a >= b);
    printf("a == b: %d\n", a==b);
    printf("a != b: %d\n", a != b);

    return 0;
}