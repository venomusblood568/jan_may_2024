// c program to illustrate the arithmetic operator
#include<stdio.h>

int main(){
    int a = 25;
    int b = 5;

    printf("a + b = %d\n",a + b);
    printf("a - n = %d\n",a-b);
    printf("a *  b = %d\n",a * b);
    printf("a / b = %d\n",a /b);
    printf("a % b = %d\n",a % b);
    printf("+a = %d\n", +a);
    printf("-a = %d\n", -a);
    printf("a++ = %d\n", a++);
    printf("a-- = %d\n", a--);

    return 0;
}